    private void mostrar()
    {
        DefaultListModel modelo = new DefaultListModel();
        dlm = oper.mostrarNoticias();
        
        
        String noticia;
        for (int i=0; i < dlm.size(); i++)
        { 
            Noticias aux = (Noticias)dlm.get(i);
            
            noticia = "<HTML>AUTOR: " + aux.getUsuarios().getNombre() + "   FECHA: " + aux.getFecha()
                    + "<br/>TITULAR: " + aux.getTitular()
                    + "<br/>TEXTO: " + aux.getTexto()
                    + "<br/>**************************************************\n</HTML>";
            
            modelo.addElement(noticia);
        }

        lNoti.setModel(modelo);
    }
